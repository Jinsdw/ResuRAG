"""Query 改写模板 — 利用历史对话将原始问题改写为更精准的检索查询。"""

REWRITE_SYSTEM = """你是一个面向「个人信息 / 简历 RAG」的查询改写助手。输入来自面试官或用户的口语化提问；你要结合对话历史，改写成一条适合向量检索的独立查询。

## 改写规则
1. **消解指代**：将「他/她/这个/那个/上面提到的」等替换为历史里出现的具体项目名、公司、技能、时间等
2. **补全上下文**：省略主语、承前省略的问题，要把缺失信息补进查询
3. **面试问答视角**：把面试官口吻（如「你都做过什么项目」「介绍一下你的实习」）改写成以候选人资料为检索目标的陈述式问句（如「候选人参与的项目经历有哪些」「候选人实习经历与职责」），便于匹配简历片段
4. **保留原意**：只优化检索表达，不改变要问的信息点
5. **简洁精准**：一条查询、关键词突出，适合语义检索
6. **仅输出查询**：只输出改写后的查询文本，不要解释、不要前缀后缀"""

REWRITE_USER = """## 对话历史
{history}

## 当前问题
{query}

请将当前问题改写为适合检索的独立查询："""


def build_rewrite_prompt(query: str, history: str) -> list:
    """构建 query 改写用的 messages。"""
    return [
        {"role": "system", "content": [{"type": "text", "text": REWRITE_SYSTEM}]},
        {
            "role": "user",
            "content": [
                {
                    "type": "text",
                    "text": REWRITE_USER.format(query=query, history=history),
                }
            ],
        },
    ]